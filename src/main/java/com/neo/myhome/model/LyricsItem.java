package com.neo.myhome.model;

public class LyricsItem {
    private String leftChorus;
    private String leftCode;
    private String leftLyrics;
    private String rightChorus;
    private String rightCode;
    private String rightLyrics;
    
	public String getLeftChorus() {
		return leftChorus;
	}
	public void setLeftChorus(String leftChorus) {
		this.leftChorus = leftChorus;
	}
	public String getLeftCode() {
		return leftCode;
	}
	public void setLeftCode(String leftCode) {
		this.leftCode = leftCode;
	}
	public String getLeftLyrics() {
		return leftLyrics;
	}
	public void setLeftLyrics(String leftLyrics) {
		this.leftLyrics = leftLyrics;
	}
	public String getRightChorus() {
		return rightChorus;
	}
	public void setRightChorus(String rightChorus) {
		this.rightChorus = rightChorus;
	}
	public String getRightCode() {
		return rightCode;
	}
	public void setRightCode(String rightCode) {
		this.rightCode = rightCode;
	}
	public String getRightLyrics() {
		return rightLyrics;
	}
	public void setRightLyrics(String rightLyrics) {
		this.rightLyrics = rightLyrics;
	}
  
}
